char *
swish_time_print(
    double time
)
{
    int hh, mm, ss;
    int delta;
    char *str;

    if (time < 0)
        time = 0;

    delta = (int)(time + 0.5);
    ss = delta % 60;
    delta /= 60;
    hh = delta / 60;
    mm = delta % 60;

    str = swish_xmalloc(9);
    if (sprintf(str, "%02d:%02d:%02d", hh, mm, ss) > 0) {
        return str;
    }
    else {
        swish_xfree(str);
        return (char *)swish_xstrdup((xmlChar *)"unknown time");
    }
}

